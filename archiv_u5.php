        </h3>
        <br>
        <h5><b>Abstract</b></h5> Es wurde eine Software-Plattform für die gemeinsame Auswertung, Darstellung und Fusion der Daten der  zerstörungsfreien  Prüfmethoden  Computertomographie  (CT),  Ultraschall  und  Thermographie entwickelt. Der Fokus lag hierbei auf der Prüfung von Kohle-(CFK) und Glasfaserverbundkunststoffen (GFK).</p>   
        <br>
        <h5><b>Dieses Projekt wurde von der EU mit über 900.000€ gefördert</b></h5>
