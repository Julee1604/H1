            <div data-aos="fade-left">
              <p> <h5> <b>Ergebnisse:</b></h5> Im Rahmen des EU-Projektes QualiFibre wurden die zerstörungsfreien Prüfmethoden CT, Ultraschall und    Thermographie    betrachtet.    Der    Fokus    lag    hierbei    auf    der    Untersuchung    von Faserverbundkunststoffen (FVK), insbesondere CFK und GFK. Jede der genannten Prüfmethoden hat ihre  spezifischen  Vor-und Nachteile.  Um  den  größtmöglichen  Mehrwert  zu  generieren,  wurde  eine Software-Plattform  für  die  gemeinsame  Auswertung,  Darstellung  und  Fusion  der  mit  den  Methoden erzeugten Daten entwickelt. Für   die   Auswertung   der   dreidimensionalen   CT-Daten   sind   in   der   Plattform   Algorithmen   zur Defekterkennung, Analyse der Faserorientierung und der Porosität enthalten. Auch  die  zweidimensionalen  Ultraschall-und Thermographiedaten  werden  automatisiert  hinsichtlich Fehlstellen  untersucht.  Da  manche  Defekte  nicht  in  beiden  Prüfbildern  enthalten  sind,  wurde  eine Datenfusion  entwickelt.  Hierdurch  wird  es  ermöglicht,  die  maximale  Aussagekraft  aus  beiden Prüftechniken zu erhalten und die Erkennungsrate der Defekte in Summe zu erhöhen.Auch wenn die entwickelten Verfahren für FVK ausgelegt wurden, sind sie dennoch auf eine Vielzahl anderer Materialarten übertragbar. </p>
