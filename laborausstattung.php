              <p >
                Das selektive Lasersintern (SLS) ist eines der am häufigsten eingesetzten industriellen additiven
                Fertigungsverfahren.
                Es ermöglicht den 3D-Druck von Bauteilen mit nahezu den Festigkeitswerten von Spritzgussteilen.
                Ausgangsmaterialien sind hierbei unterschiedliche Kunststoffe in Pulverform.
                Das Pulver wird im Prozess schichtweise aufgetragen und mit Hilfe eines Lasers aufgeschmolzen und daraus
                entstehen dreidimensionale
                Bauteile mit nahezu keinen Geometrieeinschränkungen.
                Durch den Druck im Pulverbett sind zudem keine Stützstrukturen erforderlich.
                Neben dem SLS Verfahren verwenden wir auch das Fused Deposition Modeling (FDM) Verfahren zur Generierung
                von Bauteilen.
                Es handelt sich hierbei um ein Extrusionsverfahren, bei welchem aufgeschmolzener Kunststoff als
                zähflüssiger Faden geometrisch
                definiert auf Bauplattform aufgetragen wird. Durch die Absenkung dieser und den schichtweisen Auftrag
                können so dreidimensionale
                Bauteile gefertigt werden. Dieses Verfahren eignet sich besonders für Anschauungsmuster und Bauteilen
                mit geringen Festigkeitsanforderungen.
