            <div class="container">
            <p><h4>Abstract</h4>
                  Feuerlöscher und Löschanlagen sind unumgängliche und notwendige Einrichtungen für einen sicheren
                  Personentransport. Aber nicht jedes Transportmittel verfügt über den notwendigen Stauraum für die
                  eigentlich erforderlichen Feuerlöscher bzw. Löschmittelbehälter. Insbesondere Busse sind, wie immer
                  wieder  den  Medien  zu  entnehmen,  besonders  gefährdet  (siehe  Abbildung  1).  Auch  im  Bereich  des
                  emissionsfreien Automobils, beispielsweise bei leicht entzündlichem Wasserstoff und die
                  Brandgefährdung  durch  Akkus,  ist  eine  Verbesserung  des  Brandschutzes  notwendig.  Ziel  des
                  Projektes ist es deshalb, einen in seiner Form adaptierten Druckbehälter aus Composite-Werkstoffen
                  herzustellen, d.h. es werden Seitenwände in beliebiger Form für den Löscher verwendet.</p>

