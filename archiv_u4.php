            <h3 id="überschriften" data-aos="fade-left"> Abgeschlossenes BMBF-gefördertes Forschungsprojekt zum Thema CFK-Feuerlöscher in Leichtbauweise mit Halonersatzstoff in der Luftfahrt (CFK-Löscher) <br> </h3>
