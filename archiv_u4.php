          <p> <h5><b>Abstract</b></h5> Im Rahmen eines BMBF-Projekts wurde ein leichter und zulassungsfähiger Kabinenfeuerlöscher aus kohlenstofffaserverstärktem  Kunststoff  (CFK)  für  die  Luftfahrt  entwickelt.  Die  Gewichtsreduzierung dient  dabei  der  Energieeffizienz  und  somit  auch  der  CO2-Reduzierung  im  Luftfahrtbereich. Für  die Entwicklung wurde die Thermo-und Computertomographie als Prüftechnik eingesetzt.</p>
   
