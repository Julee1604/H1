          <div>
            <p> <h5> <b>Ergebnisse:</b></h5> Klassische Kabinenfeuerlöscher bestehen aus Blech und enthalten ca. ein Kilogramm des bewährten, weil sehr wirksamen und für Menschen ungiftigen, jedoch die Ozonschicht schädigendenLöschmittels Halon. Dies führt dazu, dass Halon als Löschmittel in Flugzeugen in absehbarer Zeit ersetzt werden muss. Allerdings weisen alternative Löschmittel eine geringere Löschleistung pro Volumen auf, was zu einer  Erhöhung  der  mitgeführten  Löschmittelmenge  führt.  Durch  den  Einsatz  des  im  Projekt entwickelten leichten CFK-Löschers kann das Gesamtgewicht um bis zu 33 Prozent reduziert werden. <br> Das  bedeutet,  dass  eine  größere  Löschmittelmenge  ohne  Gewichtserhöhung  mitgeführt  bzw.  bei gleicher Löschleistung das Gesamtgewicht reduziert werden kann. <br><br> Die hergestellten Behälter wurden im Rahmen der Entwicklungsunterstützung mittels zerstörungsfreier Prüfmethoden  untersucht.  Dabei  wurden  sowohl  qualitative  als  auch  quantitative  Prüfungen  mittels Industrieller   Computertomographie   (CT)   durchgeführt.   Durch   die   ebenfalls   zerstörungsfreie Untersuchungsmethode  Thermographie  konnten  nicht  nur  Risse  in  den  Behältern  erkannt  werden, diese  Prüfmethodik  ist  im  Gegensatz  zur  CT  auch  innerhalb  einer  100  Prozent-Kontrolle  in  der Serienfertigung einsetzbar. </p>
