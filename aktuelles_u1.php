                <p><h4>Ziel des D-FMEA-Trainings</h4>

                  Die  Methode  D-  (Design-)  FMEA  (Fehlermöglichkeits-  und  Einflussanalyse)  wird  in  vielen  Branchen
                  seit  Jahren  erfolgreich  zur  Risikominimierung  im  Produktentstehungsprozess  (PEP)  eingesetzt  und
                  unterstützt damit messbar und signifikant die Senkung der Kosten in den nachgelagerten Prozessen,
                  wie zum Beispiel Serviceeinsätzen, Gewährleistungskosten, Änderungen/Anpassungen in der
                  Produktion während der Serie. </br>
                  Nach Abschluss des D- FMEA Basis-Trainings kennen die Teilnehmer die Methode Design- / Produkt-
                  FMEA.  Sie  können  die  Methode  und  deren  Vorgehensweise  richtig  im  Produktentstehungsprozess
                  einordnen.</br>
                  Die  Teilnehmer  der  Trainings  sind  in  der  Lage  nach  dem  Training  effizient  an  FMEA-Workshops
                  teilzunehmen  und  erkennen  Ihre  Rolle  darin.  Vorteile  und  Nutzen  der  Methoden  sind  vermittelt  und
                  werden durch die Teilnehmer erkannt.</p>

                <p><h4>Teilnehmer an dem D-FMEA-Training</h4>
                  Mitarbeiter  aus  unterschiedlichen  Unternehmen,  welche  die  FMEA  bereits  einsetzen,  und  welche
                  bereits erste Erkenntnisse über deren Nutzen in der praktischen Anwendung gesammelt haben.
                  Die  Teilnehmerzahl  für  die  Methodentrainings  ist  auf  insgesamt  10  Teilnehmer  aus  verschiedenen
                  Unternehmen begrenzt.</p>

                <p><h4>Ergebnisse des Projektes und des D-FMEA-Trainings</h4>
                  Die Teilnehmer des Trainings kennen die prinzipielle Vorgehensweise der Design-FMEA. Sie können
                  die sinnvolle Anwendung der D-FMEA objektiv einschätzen und zielgerichtet und effizient durchführen.
                  Die  Teilnehmer  des  Trainings  sind  in  der  Lage  eine  D-FMEA  aus  fachlicher  Sicht  zu  begleiten  und
                  kennen die theoretischen Grundlagen der Moderation einer FMEA.</p>

                <p><h4>Termin des nächsten D-FMEA-Trainings</h4>
                  Das  nächste  D-FMEA  Training  mit  Theorie  zur  Moderation  findet  am  11.  und  12.  Januar  2022  in
                  Heidenheim an der Brenz statt.</br>
