                <p><h4>Nachbereitung des D-FMEA Basis Trainings</h4>
                  <li>Nachbereitung der Trainings mit Erstellung Fotoprotokoll</li>
                  <li>Feedbackgespräch mit Auftraggeber und Klärung möglicher offener Fragen</li>
                  <li>Auswertung der Feedbackbögen</li></p>

                <p><h4>Vorabfragebögen für die Teilnehmer</h4>
                  Jeder  Teilnehmer  erhält  im  Vorfeld  einen  Fragebogen  zu  Ermittlung  seiner  Erwartungen  an  das
                  Training.  Es  soll  dabei  auch  erkannt  werden  mit  welchem  Vorwissen  die  Teilnehmer  kommen  und
                  welche Herausforderungen sie nach dem Training mit dem erlangten Wissen meistern sollen.</br>
                  Das Vorgespräch und die Rückmeldungen geben den Trainer die Möglichkeit, im Vorfeld das Training
                  auf  die  Anforderungen  der  Teilnehmer  auszurichten.  Für  die  Vorabfrage  ist  es  notwendig  die
                  Teilnehmer mit ihren Funktionen und Emailadressen benannt zu bekommen.</p>

                <p><h4>Trainingsräume und -equipment</h4>
                  Trainingsräume und -equipment wird durch Steinbeis Transferzentrum ZPM gestellt.
                  <li>Ausreichend großer Seminarraum (> 30 m²) mit U-Bestuhlung, Flipchart und Metaplantechnik
                    inkl. Moderatorenkoffer sowie Tagungstechnik (Beamer, Leinwand)</li>
                  <li>Verpflegung (Frühstückspause, Lunch, Nachmittagspause) und Getränke für die Teilnehmer</li></p>

                <p><h4>Trainingsunterlagen und Dokumentation des Trainings</h4>
                  Jeder Teilnehmer erhält eine gedruckte Seminarunterlage zur vertiefenden Nacharbeit, die
                  Präsentation  des  Referenten  als  PDF  und  eine Fotodokumentation  der Veranstaltung  im  Nachgang
                  per Email.</p>

                <p><h4>Feedbackbögen der Teilnehmer</h4>
                  Unmittelbar nach dem Training bekommt jeder Teilnehmer die Möglichkeit mündliches und schriftliches
                  Feedback dem Trainer zu der Veranstaltung zu geben. Gerne kann für das schriftliche Feedback auch
                  vorhandene Standards des Unternehmens verwendet werden.</p>

                <p><h4>Teilnahmebestätigung</h4>
                  Jeder Teilnehmer erhält nach der Veranstaltung eine persönliche Teilnahmebescheinigung.</p>

                <p><h4>Anreise</h4>
                  Die Teilnehmer organisieren die Anreise und Unterbringung am Seminarort selbst.</p>

                <p><h4>Durchführung der Trainings</h4>
                  Prof. Dr.-Ing. Nico Blessing, Leiter des ZPM und Experte für Entwicklungsprozesse, Risikoanalysen,
                  CE-Konformität, FMEA und Quality Function Deployment</p>

               <p><h4>Vereinbarung für die Abrechnung</h4>
                Die Vergütung für die Vorbereitung und Durchführung des 2-tägigen D-FMEA Trainings in Heidenheim
                an der Brenz beträgt als Festpreis € 1.200,- zzgl. MwSt. pro Teilnehmer.</p>

               <p><h4>Nichterscheinen und Rücktritt</h4>
                Kommt es aus wichtigem Grund zu Stornierung des Trainings von Seiten des Teilnehmers, werden bis
                2 Wochen vor dem Training 50% und bis eine Woche vor dem Training 90% der Teilnahmegebühr in
                Rechnung gestellt, danach 100%. Kommt es zum Ausfall des Trainers wird ein Alternativtermin für das
                Training angeboten.</p>

               <p><h4>Zahlungsfrist</h4>
                Es wird eine Zahlungsfrist von 30 Tagen nach Erhalt der Rechnung vereinbart.</p>

